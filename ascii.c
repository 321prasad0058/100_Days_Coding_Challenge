#include<stdio.h>

int main()
{
    char c;
    
    //Taking Input
    printf("Enter any character:\n");
    scanf("%c",&c);
    
    // Displays the character as well as its ASCII Value
    printf("ASCII value of %c is %d",c,c);
    
    return 0;
}


/*

Output:

Enter any character:
Z
ASCII value of Z is 90


*/